void main() {
  // Declaring a constant integer
  const int myNumber = 42;

  // Declaring a constant string
  const String myString = "Hello, Dart!";

  // Declaring a constant list
  const List<int> myNumbersList = [1, 2, 3, 4, 5];

  // Declaring a constant map
  const Map<String, int> myMap = {'one': 1, 'two': 2, 'three': 3};

  // Print the constants
  print("Number: $myNumber");
  print("String: $myString");
  print("List: $myNumbersList");
  print("Map: $myMap");
}